int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while ((i * i) != nb)
	{
		i++;
		if (i > nb || (i * i) < 0 || i < 0 || i * i > nb)
			return (0);
	}
	return (i);
}
