#include <unistd.h>

int	main(int c, char **v)
{
	int	i;
	int	j;

	j = 0;
	i = c - 1;
	while (c > 1)
	{
		while (v[i][j] != '\0')
		{
			write (1, &v[i][j], 1);
			j++;
		}
		write (1, &"\n", 1);
		j = 0;
		i--;
		c--;
	}
	return (0);
}
