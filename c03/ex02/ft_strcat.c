char	*ft_strcat(char *dest, char *src)
{
	char	*aux;

	aux = dest;
	while (*aux != '\0')
		aux++;
	while (*src != '\0')
	{
		*aux = *src;
		aux++;
		src++;
	}
	*aux = '\0';
	return (dest);
}
